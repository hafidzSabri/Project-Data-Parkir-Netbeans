    private void lblMasukMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblMasukMousePressed
        if 
            (txtNoPolisi.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Nomor Polisi Belum Diisi!");
        }
        
        else {
        String save = "INSERT INTO masukparkir(noPolisi,jnsKendaraan,tglMasuk,jamMasuk) VALUE (?,?,?,?)";
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        
        try {
            st = con.prepareStatement(save);
            st.setString(1,txtNoPolisi.getText());
            st.setString(2, (String) jComboBox1.getSelectedItem());
            st.setString(3, dtf.format(now));
            st.setString(4, jam.format(now));
                        
            st.execute();
            tampilkan_masuk();

            JOptionPane.showMessageDialog(null, "Berhasil Masuk Parkirz");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        }
    }//GEN-LAST:event_lblMasukMousePressed

