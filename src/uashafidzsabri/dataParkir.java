   private void tampilkan_masuk(){
       String perintah="SELECT * FROM masukparkir";
        try{
            st = con.prepareStatement(perintah);
            rs = st.executeQuery();
            DefaultTableModel tMasuk = new DefaultTableModel();
            tMasuk.addColumn("noTiket");
            tMasuk.addColumn("noPolisi");
            tMasuk.addColumn("jnsKendaraan");
            tMasuk.addColumn("tglMasuk");
            tMasuk.addColumn("jamMasuk");
  
            while (rs.next())
            {
                tMasuk.addRow(new Object []{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5)                    
                });
            }
            tblMasuk.setModel(tMasuk);
            
        }
        catch(Exception e) {
            System.out.println("Data Tidak Bisa Ditampilkan "+e.getMessage());
        }
   }

